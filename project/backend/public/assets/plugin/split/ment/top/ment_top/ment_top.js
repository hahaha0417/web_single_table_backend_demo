$(document).ready(function(e) {
    var $win = $(window);

    $win.on('scroll', function(e) {
        if ($(this).scrollTop() > 200) {
            $('a.go-top').addClass('view')
        } else {
            $('a.go-top').removeClass('view')
        }
    }).trigger('scroll');
    $('a.go-top').on('click', function(e) {
        $('html, body').stop().animate({
            scrollTop: 0,
        }, 1200, 'linear')
    });
});