$(function(){   
    $(window).scroll(function () {
        //scrollUp btn
        if ($(this).scrollTop() > 0) {            
            $('#asus_scroll_up').addClass('hd-show');
        } else {
            $('#asus_scroll_up').removeClass('hd-show');
        }
    }).scroll();      
});