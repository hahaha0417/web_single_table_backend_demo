$(function(){
    $("footer").click(function(){
        if($(".footer_bar").is(':visible')) {
            $(".footer_bar").fadeOut(600);
            var $body = (window.opera) ? (document.compatMode == "CSS1Compat" ? $('html') : $('body')) : $('html,body');
            $body.animate({
                scrollTop: $('footer').offset().top,
            }, 2000);
        }
        else{

            $(".footer_bar").fadeIn(600);
            var $body = (window.opera) ? (document.compatMode == "CSS1Compat" ? $('html') : $('body')) : $('html,body');
            $body.animate({
                scrollTop: $('footer').offset().top,
            }, 2000);
        }
    });
});
